def f22(x):
    ED = x & 0b11111111100000000000000000000000
    C = x & 0b00000000011111111111000000000000
    B = x & 0b00000000000000000000111111111110
    A = x & 0b00000000000000000000000000000001
    ED = ED >> 23
    C = C >> 12
    B = B >> 1
    B = B << 11
    B = B | C
    B = B << 1
    B = B | A
    B = B << 9
    B = B | ED
    return B